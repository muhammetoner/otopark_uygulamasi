private boolean isStrongPassword(String password) {
    return password.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[^a-zA-Z0-9]).{8,}$");
}
// Kullanıcı adı geçerlilik kontrolü (Türkçe karakter destekli)
private boolean isValidUsername(String username) {
    String uname = username.toLowerCase();

    // Yasaklı kelimeler
    String[] forbidden = {"admin", "root", "kullanici", "user", "test", "guest"};

    // Uzunluk kontrolü
    if (username.length() < 5 || username.length() > 20) {
        return false;
    }

    // Harfle başlamalı (Türkçe harfler dahil)
    if (!Character.isLetter(username.charAt(0))) {
        return false;
    }

    // Geçerli karakterler: harf (Türkçe dahil), rakam, alt çizgi
    if (!username.matches("^[a-zA-ZçÇğĞıİöÖşŞüÜ0-9_]+$")) {
        return false;
    }

    // Yasaklı kelime içeriyorsa
    for (String bad : forbidden) {
        if (uname.contains(bad)) {
            return false;
        }
    }

    return true;
}

