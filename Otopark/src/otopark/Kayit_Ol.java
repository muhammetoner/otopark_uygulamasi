    JOptionPane.showMessageDialog(this,
        "Geçersiz kullanıcı adı.\n" +
        "- 5-20 karakter olmalı\n" +
        "- Harfle başlamalı (Türkçe harfler dahil)\n" +
        "- Sadece harf, rakam ve alt çizgi (_) içermeli\n" +
        "- 'admin', 'kullanici', 'test' gibi kelimeler içermemeli.");
