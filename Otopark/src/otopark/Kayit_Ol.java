private boolean isValidUsername(String username) {
    String uname = username.toLowerCase();

    // YasaklÄ± kelimeler
    String[] forbidden = {"admin", "root", "kullanici", "user", "test", "guest"};

    // Uzunluk kontrolÃ¼
    if (username.length() < 5 || username.length() > 20) {
        return false;
    }

    // Harfle baÅŸlamalÄ± (TÃ¼rkÃ§e harfler dahil)
    if (!Character.isLetter(username.charAt(0))) {
        return false;
    }

    // GeÃ§erli karakterler: harf (TÃ¼rkÃ§e dahil), rakam, alt Ã§izgi
    if (!username.matches("^[a-zA-ZÃ§Ã‡ÄŸÄžÄ±Ä°Ã¶Ã–ÅŸÅžÃ¼Ãœ0-9_]+$")) {
        return false;
    }

    // YasaklÄ± kelime iÃ§eriyorsa
    for (String bad : forbidden) {
        if (uname.contains(bad)) {
            return false;
        }
    }

    return true;
}

